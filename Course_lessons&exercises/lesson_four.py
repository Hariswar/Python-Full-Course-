#Arithmetic Operators 

friends = 11

friends += 1 #same thing as friends = friends + 1

friends -= 2 #same thing as friends = friends - 1

friends *= 3 #same things as friends = friends * 3

friends /= 3 #same things as friends = friends / 3

friends **= 3 #same things as friends = friends ** 3

remainder = friends % 2
print(remainder)

print(friends)